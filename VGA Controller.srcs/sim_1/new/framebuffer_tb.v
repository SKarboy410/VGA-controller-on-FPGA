`timescale 1ns / 1ps

module framebuffer_tb;

//--------------------------------------------------
// INPUTS
//--------------------------------------------------
reg pixel_clock;
reg [9:0] pixel_x;
reg [9:0] pixel_y;
reg video_active;

//--------------------------------------------------
// OUTPUTS
//--------------------------------------------------
wire [11:0] pixel_colour;

//--------------------------------------------------
// DUT
//--------------------------------------------------
framebuffer DUT(
    .pixel_clock(pixel_clock),
    .pixel_x(pixel_x),
    .pixel_y(pixel_y),
    .video_active(video_active),
    .pixel_colour(pixel_colour)
);

//--------------------------------------------------
// CLOCK
//--------------------------------------------------
initial begin
    pixel_clock = 0;
end

always begin
    #20 pixel_clock = ~pixel_clock;
end

//--------------------------------------------------
// TEST
//--------------------------------------------------
initial begin

    //----------------------------------------------
    // preload framebuffer memory
    //----------------------------------------------
    DUT.framebuffer[0]  = 12'hF00; //red
    DUT.framebuffer[1]  = 12'h0F0; //green
    DUT.framebuffer[2]  = 12'h00F; //blue
    DUT.framebuffer[10] = 12'hFFF; //white

    //----------------------------------------------
    // initial values
    //----------------------------------------------
    pixel_x = 0;
    pixel_y = 0;
    video_active = 0;

    #100;

    //----------------------------------------------
    // TEST 1 : address 0
    //----------------------------------------------
    video_active = 1;

    pixel_x = 0;
    pixel_y = 0;

    #50;

    //----------------------------------------------
    // TEST 2 : address 1
    //----------------------------------------------
    pixel_x = 4;
    pixel_y = 0;

    #50;

    //----------------------------------------------
    // TEST 3 : address 2
    //----------------------------------------------
    pixel_x = 8;
    pixel_y = 0;

    #50;

    //----------------------------------------------
    // TEST 4 : address 10
    //----------------------------------------------
    pixel_x = 40;
    pixel_y = 0;

    #50;

    //----------------------------------------------
    // TEST 5 : video inactive
    //----------------------------------------------
    video_active = 0;

    #50;

    //----------------------------------------------
    // finish
    //----------------------------------------------
    $finish;

end


endmodule